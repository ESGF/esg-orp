package esg.orp.app;

import javax.servlet.FilterConfig;

/**
 * Allows the {@link AuthorizationFilter} to be configured with a class to transform the URL
 * before sending it to an authorization service.
 */
public interface AuthorizationFilterUrlTransformer {
	/**
	 * Method called by @AuthorizationFilter at initialization time.
	 * @param filterConfig filter configuration
	 */
	public void init(FilterConfig filterConfig);

	/**
	 * Transforms a URL to that to be sent to the authorization service.
	 * @param url input URL
	 * @return transformed URL
	 */
	String transformUrl(String url);
}
